package curs.curs19;

public class Calculator {

    int addSumOfTwo(int x,int y){
        int sum=x+y;
        return sum;
    }
    int addSumOfThree(int x,int y, int z){
        int sum=x+y+z;
        return sum;
    }
    double addSumOfTwoDouble(double x,double y){
        double sum=x+y;
        return sum;
    }

}
