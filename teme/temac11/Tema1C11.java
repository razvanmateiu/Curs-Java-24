package teme.temac11;

public class Tema1C11 {
//    -Create a Java program where you define an integer variable with a value between 1 and 7.
//    Use a switch statement to print the name of the corresponding day of the week.
//    For example, if the variable has a value of 1, it should print "Monday".
public static void main(String[] args) {
    int day=1;
    switch (day){
        case (1)-> System.out.println("Luni");
        case (2)-> System.out.println("Marti");
        case (3)-> System.out.println("Miercuri");
        case (4)-> System.out.println("Joi");
        case (5)-> System.out.println("Vineri");
        case (6)-> System.out.println("Sambata");
        case (7)-> System.out.println("Duminica");

    }
}

}
