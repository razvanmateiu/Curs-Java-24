package teme.temac11;

public class Tema3C11 {
    public static void main(String[] args) {
//        -Create a Java program that uses a do-while loop to print numbers from 10 to 1 in descending order.
        int i=10;
        do {
            System.out.println(i);
            --i;
        } while (i>=0);
    }
}
